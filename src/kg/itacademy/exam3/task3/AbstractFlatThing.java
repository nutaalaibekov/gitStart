package kg.itacademy.exam3.task3;

public class AbstractFlatThing extends AbstractThing {
    private double width;
    private double height;

    public AbstractFlatThing(String name, double width, double height) {
        super(name);
        this.width = width;
        this.height = height;
    }

    public double getWidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }
}
